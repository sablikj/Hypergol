    <h1 class="text-xl font-bold leading-6 font-medium pb-2 text-gray-900">About RocketLab</h1>
    <p class="mt-1 text-gray-900 sm:mt-0">Rocket Lab is a private American aerospace manufacturer and small satellite launch service provider with a wholly owned New Zealand subsidiary.</p>
    <h3 class="mt-3 font-semibold text-lg">Reusability</h3>
    <p class="mt-1 text-gray-900 sm:mt-0">
        Rocket Lab began to develop reusable first-stage technology in 2018, after previously stating publicly that they had no intention of attempting to recover and then reuse their small low-cost launch vehicles.
        They disclosed the effort to study the potential recovery of an Electron first stage in August 2019, eventually aiming to use a parachute and mid-air retrieval.
