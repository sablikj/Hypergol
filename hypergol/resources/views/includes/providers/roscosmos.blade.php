    <h1 class="text-xl font-bold leading-6 font-medium pb-2 text-gray-900">About Roscosmos</h1>
    <p class="mt-1 text-gray-900 sm:mt-0">Roscosmos, also known as the Roscosmos State Corporation for Space Activities, is the coordinating hub for space activities in Russia. It performs numerous civilian activities
        (including Earth monitoring and the astronaut program) and coordinates with the Defense Ministry of the Russian Federation for military launches.</p>
    <p class="mt-1 text-gray-900 sm:mt-0">
        As the main successor to the Soviet space program, Roscosmos' legacy includes the world's first satellite, first human spaceflight,
        and first space station. Its current activities include the International Space Station, wherein it is a major partner. On February 22, 2019,
        Roscosmos announced the construction of its new headquarters in Moscow, the National Space Centre.
    </p>
